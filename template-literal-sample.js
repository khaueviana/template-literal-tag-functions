var p = {
    name: 'Jackson',
    nn: 'Jak'
}

//String Concatenation
console.log('Hi, I\'m ' + p.name + '! Call me "' + p.nn + '".');

//Template Literals
console.log(`Hi, I'm ${p.name}! Call me "${p.nn}".`);

// STRING CONCATENATION
console.log("Dear Mom,\n" + 
"Hope you are well.\n" + 
"\tLove, your son")

// TEMPLATE LITERALS
console.log(`Dear Mom,
Hope you are well.
    Love, your son`);

// Dear Mom,
// Hope you are well.
//     Love, your son

// STRING CONCATENATION
console.log('Three plus six is ' + (3 + 6) + '.');
// TEMPLATE LITERALS
console.log(`Three plus six is ${3 + 6}.`);
// "Three plus six is 9."